package ru.code4a.graphql.relay.schema.enums

/**
 * GraphQL enum for specifying sort order direction.
 */
enum class OrderTypeGQLEnum {
  /**
   * Ascending order (A to Z, oldest to newest, lowest to highest)
   */
  ASC,

  /**
   * Descending order (Z to A, newest to oldest, highest to lowest)
   */
  DESC
}
